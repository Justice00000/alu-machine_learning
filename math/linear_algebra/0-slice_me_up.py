#!/usr/bin/env python3
arr = [9, 8, 2, 3, 9, 4, 1, 0, 3]
arr1 = arr[:2] # Get first two elements (0-based indexing)
arr2 = arr[-5:] # Get last five elements
arr3 = arr[1:6] # Get elements from index 1 (inclusive) to 5 (exclusive)
print("The first two numbers of the array are:", arr1)
print("The last five numbers of the array are:", arr2)
print("The 2nd through 6th numbers of the array are:", arr3)
